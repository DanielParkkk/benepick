// ── 언어 드롭다운 옵션 업데이트 ──────────────────────────────
function _updateDropdownOptions() {
  const options = document.querySelectorAll('#langDropdown .lang-option');
  const flagMap = { ko:'🇰🇷', en:'🇺🇸', zh:'🇨🇳', ja:'🇯🇵', fil:'🇵🇭' };
  const codeOrder = ['ko','en','zh','ja','fil'];

  if (options.length >= 5) {
    codeOrder.forEach((code, i) => {
      if (!options[i]) return;
      const display = LANG_DISPLAY_MAP[code];
      options[i].setAttribute('data-lang-code', code);
      const flagEl = options[i].querySelector('.lang-flag');
      const textEl = options[i].querySelectorAll('span')[1];
      if (flagEl) flagEl.textContent = flagMap[code];
      if (textEl) textEl.textContent = display;
      // Update onclick
      options[i].setAttribute('onclick', `selectLanguage(this, '${display}')`);
    });
  }
}

// ── 핵심: 페이지 텍스트 교체 ──────────────────────────────────
function applyTranslations(lang) {
  // 기본 번역 객체 + 동적 키(연령/지역/소득) 병합
  const t = Object.assign(
    {},
    TRANSLATIONS[lang] || {},
    _buildAgeKeys(lang),
    REGION_TRANSLATIONS[lang] || {},
    INCOME_TRANSLATIONS[lang] || {}
  );
  if (!Object.keys(t).length) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      // innerHTML 허용 태그 (팁 텍스트 등 <strong> 포함)
      if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // placeholder 처리
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // <option data-i18n-opt="..."> 처리 — select 옵션 텍스트 교체
  document.querySelectorAll('[data-i18n-opt]').forEach(opt => {
    const key = opt.getAttribute('data-i18n-opt');
    if (t[key] !== undefined) opt.textContent = t[key];
  });

  // select 표시값 강제 갱신 — 옵션 텍스트 교체 후 선택된 값이 화면에 반영되도록
  document.querySelectorAll('select').forEach(sel => {
    const idx = sel.selectedIndex;
    sel.selectedIndex = -1;
    // 마이크로태스크 후 복원 (브라우저 강제 리렌더)
    requestAnimationFrame(() => {
      sel.selectedIndex = idx >= 0 ? idx : 0;
    });
  });

  // html lang 속성
  document.documentElement.lang = lang === 'ko' ? 'ko'
    : lang === 'en' ? 'en'
    : lang === 'zh' ? 'zh-Hans'
    : lang === 'ja' ? 'ja'
    : 'fil';

  // 드롭다운 레이블
  const label = document.getElementById('currentLangLabel') || document.getElementById('currentLangText');
  if (label) label.textContent = LANG_DISPLAY_MAP[lang] || lang;
}

// ── localStorage 저장/로드 ────────────────────────────────────
function saveLang(lang) {
  try { localStorage.setItem('benefic_lang', lang); } catch(e) {}
}

function loadLang() {
  try { return localStorage.getItem('benefic_lang') || 'ko'; } catch(e) { return 'ko'; }
}

// ── 언어 선택 — window에 명시 할당해서 main.js 버전을 확실히 덮어씀 ──
window.selectLanguage = function(el, langDisplay) {
  // 체크마크 갱신
  document.querySelectorAll('#langDropdown .lang-option').forEach(opt => {
    opt.classList.remove('active');
    const chk = opt.querySelector('.lang-check');
    if (chk) chk.remove();
  });
  el.classList.add('active');
  el.insertAdjacentHTML('beforeend',
    `<svg class="lang-check" viewBox="0 0 13 13" fill="none">
      <path d="M2 6.5l3.5 3.5 5.5-6" stroke="currentColor" stroke-width="1.7" stroke-linecap="round" stroke-linejoin="round"/>
    </svg>`
  );

  const langCode = LANG_CODE_MAP[langDisplay] || 'ko';
  applyTranslations(langCode);
  saveLang(langCode);

  // 드롭다운 닫기
  setTimeout(() => {
    const dd = document.getElementById('langDropdown');
    const btn = document.getElementById('langBtn');
    if (dd) dd.classList.remove('visible');
    if (btn) { btn.classList.remove('open'); btn.setAttribute('aria-expanded', 'false'); }
  }, 150);
};

// ── 초기화 ─────────────────────────────────────────────────────
function initLanguage() {
  _updateDropdownOptions();
  const saved = loadLang();
  applyTranslations(saved);

  // 드롭다운에서 현재 선택 표시
  const targetDisplay = LANG_DISPLAY_MAP[saved] || '한국어';
  document.querySelectorAll('#langDropdown .lang-option').forEach(opt => {
    opt.classList.remove('active');
    const chk = opt.querySelector('.lang-check');
    if (chk) chk.remove();
    const textEl = opt.querySelectorAll('span')[1];
    if (textEl && textEl.textContent === targetDisplay) {
      opt.classList.add('active');
      opt.insertAdjacentHTML('beforeend',
        `<svg class="lang-check" viewBox="0 0 13 13" fill="none">
          <path d="M2 6.5l3.5 3.5 5.5-6" stroke="currentColor" stroke-width="1.7" stroke-linecap="round" stroke-linejoin="round"/>
        </svg>`
      );
    }
  });
}

// ── 초기화 타이밍 ─────────────────────────────────────────────
// main.js의 window.addEventListener('load', ...) 이후에 번역이 적용되어야
// progress bar 애니메이션 등 main.js 초기화와 충돌하지 않음.
// i18n.js는 main.js 뒤에 로드되므로 window load 이벤트 리스너를 추가.
window.addEventListener('load', () => {
  initLanguage();
});

// 만약 이미 로드된 상태라면 즉시 실행 (예: 동적 삽입)
if (document.readyState === 'complete') {
  initLanguage();
}



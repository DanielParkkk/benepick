// 각 언어별 포맷 함수: (n) => "표시 텍스트"
const AGE_FORMAT = {
  ko: n => n === 65 ? '만 65세 이상' : `만 ${n}세`,
  en: n => n === 65 ? 'Age 65+' : `Age ${n}`,
  zh: n => n === 65 ? '65岁以上' : `${n}岁`,
  ja: n => n === 65 ? '65歳以上' : `${n}歳`,
  fil: n => n === 65 ? '65 pataas ang edad' : `${n} taong gulang`,
};

function _buildAgeKeys(lang) {
  const fmt = AGE_FORMAT[lang] || AGE_FORMAT.ko;
  const keys = {};
  for (let age = 18; age <= 64; age++) {
    keys[`opt_age_${age}`] = fmt(age);
  }
  keys['opt_age_65p'] = fmt(65);
  return keys;
}


function _buildAgeKeys(lang) {
  const fmt = AGE_FORMAT[lang] || AGE_FORMAT.ko;
  const keys = {};
  for (let age = 18; age <= 64; age++) {
    keys[`opt_age_${age}`] = fmt(age);
  }
  keys['opt_age_65p'] = fmt(65);
  return keys;
}

